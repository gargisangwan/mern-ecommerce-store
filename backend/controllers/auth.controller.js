export const forgotPassword = async (req, res) => {
  try {
    const { email } = req.body;

    // Check if user exists
    const user = await User.findOne({ email });
    if (!user) {
      return res.status(404).json({ message: "User not found" });
    }

    // Generate temporary password and hash it
    const newPassword = Math.random().toString(36).slice(-8); // 8-character temp password
    // const hashedPassword = await bcrypt.hash(newPassword, 10);

    // Save hashed password to the database
    user.password = newPassword;
    await user.save();

    // Create Nodemailer transport with SendGrid
    const transporter = nodemailer.createTransport(
      sgTransport({
        auth: {
          api_key: process.env.SENDGRID_API_KEY,
        },
      })
    );
    console.log("process.env.EMAIL_FROM", process.env.EMAIL_FROM);

    // Send the email
    await transporter.sendMail({
      from: process.env.EMAIL_FROM,
      to: user.email,
      subject: "Your Temporary Password",
      html: `
		  <p>Hi ${user.name || ""},</p>
		  <p>Your temporary password is: <strong>${newPassword}</strong></p>
		  <p>Please use it to log in and change your password immediately.</p>
		`,
    });

    return res
      .status(200)
      .json({ message: "Temporary password sent to your email." });
  } catch (error) {
    console.error("Forgot Password Error:", error);
    return res.status(500).json({ message: "Internal server error" });
  }
};

// Reset Password (after login)
export const resetPassword = async (req, res) => {
  try {
    const { currentPassword, newPassword } = req.body;
    const userId = req.user._id;

    // Find user by ID
    const user = await User.findById(userId);

    if (!user) {
      return res.status(404).json({ message: "User not found" });
    }

    // Compare current password with the one in the database
    const isMatch = await bcrypt.compare(currentPassword, user.password);

    if (!isMatch) {
      return res.status(400).json({ message: "Incorrect current password" });
    }

    // Hash and update to new password
    user.password = newPassword;
    await user.save();

    res.status(200).json({ message: "Password updated successfully" });
  } catch (error) {
    console.error("Reset password error:", error);
    res.status(500).json({ message: "Internal server error" });
  }
};





